    private void btnVERPASAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVERPASAActionPerformed
        int f = tab_Viaje.getSelectedRow();
        if (f == -1) {
            JOptionPane.showMessageDialog(null, "Elija una fila");
            return;
        } else {
            frmGrabar.nrovia = tab_Viaje.getValueAt(f, 0).toString();
            frmGrabar.ruta = (String) cbRutas.getSelectedItem();
            frmGrabar.costo = Double.parseDouble(tab_Viaje.getValueAt(f, 2).toString());
            frmGrabar fr = new frmGrabar();
            fr.setLocationRelativeTo(null);
            fr.setVisible(true);
            this.setVisible(false);
        }


    }//GEN-LAST:event_btnVERPASAActionPerformed
    void imagen(String cad) {
        ImageIcon imgIcon = new ImageIcon(getClass().getResource("/imagenes/" + cad + ".jpg"));
        //asignar la imagen con el tama√±o de la etiqueta
        Image imgEscala = imgIcon.getImage().getScaledInstance(
                lbimg.getWidth(), lbimg.getHeight(), Image.SCALE_SMOOTH);
        //mostrar la imagen en la etiqueta
        //Icon iconoEscala= new ImageIcon(imgEscala);
        lbimg.setIcon(new ImageIcon(imgEscala));
    }

