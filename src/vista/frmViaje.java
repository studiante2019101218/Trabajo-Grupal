    void muestraRutas() {
        cbRutas.removeAllItems();
        for (Ruta x : obj.ListRuta()) {
            cbRutas.addItem(x.getRutnom());
        }
    }

    void muestraViaje(String cod) {
        DefaultTableModel dt = (DefaultTableModel) tab_Viaje.getModel();
        dt.setRowCount(0);
        for (Viaje x : obj.ListViaje(cod)) {
            Object v[] = {x.getVianro(), x.getViafh(), x.getCostvia()};
            dt.addRow(v);
        }
    }

