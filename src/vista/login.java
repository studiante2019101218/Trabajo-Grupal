    private void bnentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnentrarActionPerformed
        //desencriptar contrasenia de password
        
        String passwtext= new String(txtpassword.getPassword());
        Usuario us = new Usuario();
        if (!txtusuario.getText().equals("") && !passwtext.equals("")) {
            us.setCode(txtusuario.getText());
            us.setClave(passwtext);
            if (obj.Login(us)) {
                frmViaje m = new frmViaje();
                m.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Llene sus datos");
        }
    }//GEN-LAST:event_bnentrarActionPerformed

