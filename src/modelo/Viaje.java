public class Viaje {
    private String vianro;
    private int busnro;
    private String rutcod_via;
    private String idcod;
    private String viahr;
    private String viafh;
    private double costvia;

    public String getVianro() {
        return vianro;
    }

    public void setVianro(String vianro) {
        this.vianro = vianro;
    }

    public int getBusnro() {
        return busnro;
    }

    public void setBusnro(int busnro) {
        this.busnro = busnro;
    }

    public String getRutcod_via() {
        return rutcod_via;
    }

    public void setRutcod_via(String rutcod_via) {
        this.rutcod_via = rutcod_via;
    }

    public String getIdcod() {
        return idcod;
    }

    public void setIdcod(String idcod) {
        this.idcod = idcod;
    }

    public String getViahr() {
        return viahr;
    }

    public void setViahr(String viahr) {
        this.viahr = viahr;
    }

    public String getViafh() {
        return viafh;
    }

    public void setViafh(String viafh) {
        this.viafh = viafh;
    }

    public double getCostvia() {
        return costvia;
    }

    public void setCostvia(double costvia) {
        this.costvia = costvia;
    }
    
    
}
