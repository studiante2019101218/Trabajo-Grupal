public class Ruta {
    private String rutcod;
    private String rutnom;
    private double pagocho;

    public String getRutcod() {
        return rutcod;
    }

    public void setRutcod(String rutcod) {
        this.rutcod = rutcod;
    }

    public String getRutnom() {
        return rutnom;
    }

    public void setRutnom(String rutnom) {
        this.rutnom = rutnom;
    }

    public double getPagocho() {
        return pagocho;
    }

    public void setPagocho(double pagocho) {
        this.pagocho = pagocho;
    }
    
    
    
}
